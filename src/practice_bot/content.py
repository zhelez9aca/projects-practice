SPOTIFY_TEXT = """
Spotify-провайдер в TrackSwop использует OAuth2 через браузер. В документации
описаны шаги: создать приложение в Spotify Developer Dashboard, получить
Client ID и Client Secret, настроить Redirect URI и пройти авторизацию.

Поддерживаемые операции:
- чтение пользовательских плейлистов;
- получение треков из плейлиста;
- создание новых плейлистов;
- добавление треков в плейлист;
- экспорт в другие сервисы.

Токены кешируются и хранятся в зашифрованном виде через Fernet.
""".strip()

VK_TEXT = """
VK Music в TrackSwop работает через token-based авторизацию: нужны token и user_id.
Провайдер получает плейлисты пользователя, читает треки, создаёт плейлисты и
добавляет в них найденные треки.

Если прямой доступ к трекам плейлиста недоступен, код использует fallback:
поиск по названию плейлиста или трека через VK API.
""".strip()

