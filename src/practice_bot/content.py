SECURITY_TEXT = """
Безопасность в TrackSwop завязана на TokenStore:
- токены шифруются через Fernet перед записью в tokens.json;
- ключ берётся из переменной окружения TRACKSWOP_MASTER_KEY;
- если ключ не задан, в dev-режиме генерируется временный ключ с предупреждением;
- документация Spotify отдельно рекомендует не коммитить credentials и хранить
  чувствительные данные в переменных окружения.
""".strip()

